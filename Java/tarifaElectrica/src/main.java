    private void buttonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalcularActionPerformed
        // TODO add your handling code here:
        
        
        
        //ENTRADA DE DATOS SOBRE LA INSTALACION LOS CONSUMOS
        try {
            
        double tension;
        if (Boolean.valueOf(radio230v.getText())){
            tension = 230;
        }else{
            tension = 400;
        }
        
        double potenciaContratada = (inputPotenciacontratada.getText().equals("")) ? 0 : Double.valueOf(inputPotenciacontratada.getText());
        
        boolean dhValle = Boolean.valueOf(boxValle.getText());
        boolean dhPunta = Boolean.valueOf(boxPunta.getText());
        boolean dhLlano = Boolean.valueOf(boxLlano.getText());
        boolean dhSuperValle = Boolean.valueOf(boxSuperValle.getText());
        boolean dhSuperPunta  = Boolean.valueOf(boxSuperPunta.getText());
        
        int periodoFactura = (inputPeriodoFactura.getText().equals("")) ? 0 : Integer.valueOf(inputPeriodoFactura.getText());
        
        double alquilerEquipo = (inputAlquilerequipo.getText().equals("")) ? 0 : Double.valueOf(inputAlquilerequipo.getText());
        
        
        
        
        
        
        // CONSUMO DEL PERIODO
        double consumoP1 = (inputConsumoP1.getText().equals("")) ? 0 : Double.valueOf(inputConsumoP1.getText());
        double consumoP2 = (inputConsumoP2.getText().equals("")) ? 0 : Double.valueOf(inputConsumoP2.getText());
        double consumoP3 = (inputConsumoP3.getText().equals("")) ? 0 : Double.valueOf(inputConsumoP3.getText());
        double consumoP4 = (inputConsumoP4.getText().equals("")) ? 0 : Double.valueOf(inputConsumoP4.getText());
        double consumoP5 = (inputConsumoP5.getText().equals("")) ? 0 : Double.valueOf(inputConsumoP5.getText());
        
        // PRECIO DE LA POTENCIA
        double potenciaP1 = (inputPotenciaP1.getText().equals("")) ? 0 : Double.valueOf(inputPotenciaP1.getText());
        double potenciaP2 = (inputPotenciaP2.getText().equals("")) ? 0 : Double.valueOf(inputPotenciaP2.getText());
        double potenciaP3 = (inputPotenciaP3.getText().equals("")) ? 0 : Double.valueOf(inputPotenciaP3.getText());
        double potenciaP4 = (inputPotenciaP4.getText().equals("")) ? 0 : Double.valueOf(inputPotenciaP4.getText());
        double potenciaP5 = (inputPotenciaP5.getText().equals("")) ? 0 : Double.valueOf(inputPotenciaP5.getText());
        
        //PRECIO DE LA ENERGIA
        double energiaP1 = (inputEnergiaP1.getText().equals("")) ? 0 : Double.valueOf(inputEnergiaP1.getText());
        double energiaP2 = (inputEnergiaP2.getText().equals("")) ? 0 : Double.valueOf(inputEnergiaP2.getText());
        double energiaP3 = (inputEnergiaP3.getText().equals("")) ? 0 : Double.valueOf(inputEnergiaP3.getText());
        double energiaP4 = (inputEnergiaP4.getText().equals("")) ? 0 : Double.valueOf(inputEnergiaP4.getText());
        double energiaP5 = (inputEnergiaP5.getText().equals("")) ? 0 : Double.valueOf(inputEnergiaP5.getText());
        

        
        

        // CALCULO DEL PRECIO POR LA POTENCIA
        double precioPotenciaP1 = potenciaContratada * potenciaP1 * periodoFactura;
        double precioPotenciaP2 = potenciaContratada * potenciaP2 * periodoFactura;
        double precioPotenciaP3 = potenciaContratada * potenciaP3 * periodoFactura;
        double precioPotenciaP4 = potenciaContratada * potenciaP4 * periodoFactura;
        double precioPotenciaP5 = potenciaContratada * potenciaP5 * periodoFactura;
        
        
        
        
        labelPrecioPotenciaP1.setText("P1: "+potenciaContratada+" kW x "+potenciaP1+" €/kW x "+periodoFactura+" Días = "+precioPotenciaP1+" €");
        labelPrecioPotenciaP2.setText("P2: "+precioPotenciaP2+" €");
        labelPrecioPotenciaP3.setText("P3: "+precioPotenciaP3+" €");
        labelPrecioPotenciaP4.setText("P4: "+potenciaContratada+" x "+potenciaP4+" x "+periodoFactura+" = "+precioPotenciaP4+" €");
        labelPrecioPotenciaP5.setText("P5: "+precioPotenciaP5+" €");
        
        
        // CALCULO DEL PRECIO POR LA ENERGIA CONSUMIDA
        double precioEnergiaP1 = consumoP1 * energiaP1;
        double precioEnergiaP2 = consumoP2 * energiaP2;
        double precioEnergiaP3 = consumoP3 * energiaP3;
        double precioEnergiaP4 = consumoP4 * energiaP4;
        double precioEnergiaP5 = consumoP5 * energiaP5;
        
        labelPrecioEnergiaP1.setText("P1: "+consumoP1+" kWh x "+energiaP1+" € kWh = "+precioEnergiaP1+" €");
        labelPrecioEnergiaP2.setText("P2: "+precioEnergiaP2+" €");
        labelPrecioEnergiaP3.setText("P3: "+precioEnergiaP3+" €");
        labelPrecioEnergiaP4.setText("P1: "+consumoP4+" x "+energiaP4+" = "+precioEnergiaP4+" €");
        labelPrecioEnergiaP5.setText("P5: "+precioEnergiaP5+" €");
        
        
        // CALCULAMOS LA FACTURA
        double precioPotenciaPtotal = precioPotenciaP1 + precioPotenciaP2 +precioPotenciaP3 + precioPotenciaP4 + precioPotenciaP5;
        double precioEnergiaPtotal = precioEnergiaP1 + precioEnergiaP2 +precioEnergiaP3 + precioEnergiaP4 + precioEnergiaP5;
        
        double impuestoElectrico = (precioPotenciaPtotal + precioEnergiaPtotal) * 0.05113;
        double alquilerequipos = alquilerEquipo *  periodoFactura;
        
        double subtotal = precioPotenciaPtotal + precioEnergiaPtotal + alquilerequipos;
        
        double total = subtotal + impuestoElectrico;
                
        double impuestoIva = total * 0.21;
        
        double TOTAL = total + impuestoIva;
        
        labelPrecioPotenciaPtotal.setText("Término potencia: "+precioPotenciaPtotal+" €");
        labelPrecioEnergiaPtotal.setText("Término energía: "+precioEnergiaPtotal+" €");
        
        labelImpuestoelectrico.setText("Impuesto eléctrico: "+impuestoElectrico+" €");
        labelAlquilerequipos.setText("Alquiler equipos: "+alquilerequipos+" €");
        
        
        labelSubtotal.setText("Subtotal: \t\t"+subtotal+" €");
        
        labelTotal.setText("Total: "+total+" €");
        
        labelImpuestoIva.setText("IVA (21%): "+impuestoIva+" €");
        
        labelTOTAL.setText(""+TOTAL+" €");
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hay errores");
        }
        
    }//GEN-LAST:event_buttonCalcularActionPerformed

